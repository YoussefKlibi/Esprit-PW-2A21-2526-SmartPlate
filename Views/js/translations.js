// ────────────────────────────────────────────────────────────────────
//  Engine
// ────────────────────────────────────────────────────────────────────
function applyLanguage(lang) {
    if (!translations[lang]) return;
    const t = translations[lang];

    // Direction RTL pour l'arabe
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');

    // Mettre à jour le titre de la page si défini
    if (t.dashboard_title && window.location.href.includes('dashboard')) document.title = t.dashboard_title;
    if (t.profile_title && window.location.href.includes('user_profile'))  document.title = t.profile_title;

    // Texte classique
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] === undefined) return;
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = t[key];
        } else {
            el.innerHTML = t[key];
        }
    });

    // Attribut title (infobulle)
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (t[key] !== undefined) el.title = t[key];
    });

    // Placeholder autonome
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key] !== undefined) el.placeholder = t[key];
    });

    // Mettre à jour le sélecteur visuel
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('lang-active', btn.dataset.lang === lang);
    });
}

function changeLanguage(lang) {
    localStorage.setItem('smartplate_lang', lang);
    applyLanguage(lang);
}

document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('smartplate_lang') || 'fr';
    applyLanguage(savedLang);
});
